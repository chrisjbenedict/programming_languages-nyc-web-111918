def reformat_languages(languages)
  new_hash = {}
  languages.each do |key, nested_hash|
    nested_hash.each do |name, props|
      new_hash[name] ||= props
      new_hash[name][:style] = []
      new_hash[name][:style] << key
    end
  end
  new_hash
end
